  function generateSchedule(month) {
    const weeks = timetableData[month];
    return weeks.map(week => `
      <div class="week-box">
        <div class="week-title">${week.week}</div>
        <ul class="classes-list">
          ${week.classes.map(cls => `<li>${cls}</li>`).join('')}
        </ul>
      </div>
    `).join('');
  }
  
  document.addEventListener('DOMContentLoaded', () => {
    const timetableContainer = document.getElementById('timetable');
    const buttons = document.querySelectorAll('.tab-button');
  
    buttons.forEach(button => {
      button.addEventListener('click', () => {
        buttons.forEach(btn => btn.classList.remove('active'));
        button.classList.add('active');
        const month = button.getAttribute('data-month');
        timetableContainer.innerHTML = generateSchedule(month);
      });
    });
  
    // Load default timetable (July 2024)
    buttons[0].click();
  });
  
